// Ping command tests the Latency (ms) between The user and the Discord Server.
if (command === 'ping') {  
    if (!message.member.roles.cache.has(managerRole)) {
        return;
    }     
    message.channel.send(`Pong! \`${Date.now() - message.createdTimestamp} ms\``); 
}

